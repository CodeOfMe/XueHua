def _build_catalogs() -> Dict[str, Dict[str, str]]:
    catalogs: Dict[str, Dict[str, str]] = {"zh": dict(_BASE_ZH), "en": dict(_BASE_EN)}
    for code, overrides in _OVERRIDES.items():
        base = dict(_BASE_EN)
        base.update(overrides)
        catalogs[code] = base
    return catalogs


CATALOGS: Dict[str, Dict[str, str]] = _build_catalogs()


def get_catalog(ui_language: str) -> Dict[str, str]:
    """Return the i18n catalog for a UI language, falling back to English."""
    if not is_ui_supported(ui_language):
        ui_language = "en"
    return dict(CATALOGS.get(ui_language, CATALOGS["en"]))


def translate(key: str, ui_language: str = "en", **kwargs) -> str:
    """Translate a key, interpolating {placeholders} from kwargs."""
    catalog = get_catalog(ui_language)
    text = catalog.get(key, key)
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text